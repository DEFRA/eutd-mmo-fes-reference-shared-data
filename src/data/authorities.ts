export const getDevolvedAuthority: (flag: string, adminPort: string) => string = (flag: string, adminPort: string) => {
  if (flag === 'GGY')
    return "Guernsey";

  if (flag === 'JEY')
    return "Jersey";

  return adminPortToDa[adminPort] ? adminPortToDa[adminPort].authority : 'England';
}

export const postCodeDaLookup = (postCodeToDa) =>
  (postCode) => {

    const postCodeArea = /^[a-z]+/i.exec(postCode)
    if (postCodeArea) {
      const value = postCodeToDa[postCodeArea[0].toUpperCase()]
      return value ? value.authority : 'England'
    } else {
      return 'England'
    }
  }
